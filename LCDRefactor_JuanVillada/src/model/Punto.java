package model;

/**
 * Representa las coordenadas de un punto dentro de una matriz
 */
public class Punto {

    //Indica el indice de fila en la matriz
    private int i;

    //Indica el indice de columna en la matriz
    private int j;


    public int getI() {

        return i;
    }

    public void setI(int i) {

        this.i = i;
    }

    public int getJ() {

        return j;
    }

    public void setJ(int j) {

        this.j = j;
    }
}